Language languageFromCode(const QString& code) {
  const QString normalized = code.trimmed();
  if (normalized.compare(QStringLiteral("zh"), Qt::CaseInsensitive) == 0 ||
      normalized.startsWith(QStringLiteral("zh_CN"), Qt::CaseInsensitive) ||
      normalized.startsWith(QStringLiteral("zh-Hans"), Qt::CaseInsensitive)) {
    return Language::kSimplifiedChinese;
  }
  if (normalized.startsWith(QStringLiteral("zh_TW"), Qt::CaseInsensitive) ||
      normalized.startsWith(QStringLiteral("zh-Hant"), Qt::CaseInsensitive)) {
    return Language::kTraditionalChinese;
  }
  for (const auto& item : kLanguages) {
    if (item.code.compare(normalized, Qt::CaseInsensitive) == 0) {
      return item.language;
    }
  }
  return Language::kEnglish;
}

QString languageCode(Language language) {
  for (const auto& item : kLanguages) {
    if (item.language == language) {
      return item.code;
    }
  }
  return QStringLiteral("en");
}

QString languageDisplayName(Language language) {
  for (const auto& item : kLanguages) {
    if (item.language == language) {
      return item.display_name;
    }
  }
  return QStringLiteral("English");
}

const std::vector<LanguageItem>& supportedLanguages() {
  return kLanguages;
}

QString tr(const QString& key) {
  if (key.isEmpty()) {
    return key;
  }

  const Dict& current = dictFor(g_language);
  if (const QString* value = findInDict(current, key); value != nullptr) {
    return *value;
  }

  const Dict& english = englishDict();
  if (const QString* value = findInDict(english, key); value != nullptr) {
    return *value;
  }

  return key;
}

